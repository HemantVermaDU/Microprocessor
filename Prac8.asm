.MODEL SMALL
.DATA
D1 DB 13,10,'Enter the element:$'
D2 DB 13,10,'Enter the element to be searched:$'
D3 DB 13,10,'Element not found.$'
D4 DB 13,10,'Element found.$'
D5 DB 13,10,'Enter the size of the array:$'
ARRAY DB 20 DUP(?)
TEMP DB ?
L DB ?
U DB ?

.CODE
.STARTUP


MOV DX,OFFSET D5
MOV AH,09H
INT 21H

MOV AH,01H
INT 21H
SUB AL,30H
MOV BL,AL


MOV TEMP,BL
DEC BL
MOV U,BL

MOV CH,0
MOV CL,TEMP
MOV DX,OFFSET D1
MOV SI,0

L1:
MOV AH,09H
INT 21H
MOV AH,01H
INT 21H
SUB AL,30H
MOV BL,AL
INT 21H
SUB AL,30H
SHL BL,4
 
ADD BL,AL
MOV ARRAY[SI],BL
INC SI
LOOP L1

MOV DX,OFFSET D2
MOV AH,09H
INT 21H
MOV AH,01H
INT 21H
SUB AL,30H

MOV BL,AL
INT 21H
SUB AL,30H
SHL BL,4
ADD BL,AL

MOV CL,TEMP
MOV L,0H
L2:
MOV AH,0H
MOV AL,L
ADD AL,U
MOV BH,02H
DIV BH
MOV AH,0H
MOV SI,AX
CMP ARRAY[SI],BL
JA A
JB B
MOV DX,OFFSET D4
MOV AH,09H
INT 21H
JMP E
MOV AX,SI
A:
MOV U,AL
JMP L9
B:
MOV L,AL

L9:
MOV AL,L
CMP AL,U
 
LOOPNE L2

MOV DX,OFFSET D3
MOV AH,09H
INT 21H
JMP E

E:
.EXIT
END
