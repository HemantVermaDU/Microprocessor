.MODEL SMALL
.386P

.DATA ;
N1 DD 1234567AH
N2 DD 11111111H
N3 DD ?
N4 DD ?
COUNT DB 08H

QUOTIENT DB "QUOTIENT:", 10, 13, '$'
REMAINDER DB "REMAINDER:", 10, 13, '$'
 
.CODE ;
.STARTUP ;
MOV AX, @DATA
MOV DS, AX
MOV EDX, 00000000H
MOV EAX, N1
DIV N2
MOV N3, EDX
MOV N4, EAX

MOV DX, OFFSET QUOTIENT
MOV AH, 09H
INT 21H

MOV EAX, N4
CALL DISP
MOV DL, 13
MOV AH, 02H
INT 21H
MOV DL, 10
MOV AH, 02H
INT 21H

MOV DX, OFFSET REMAINDER
MOV AH, 09H
INT 21H

MOV EAX, N3
CALL DISP
MOV AH, 4CH
INT 21H

DISP PROC NEAR
MOV CL, COUNT
UP:
ROL EAX, 04H
MOV BX, AX
AND AL, 0FH
CMP AL, 0AH
JNGE NOTGREATER
ADD AL, 07H
NOTGREATER:
ADD AL, 30H
MOV DL, AL
MOV AH, 02H
INT 21H

MOV AX, BX
LOOP UP
RET 

ENDP DISP
END
.EXIT
