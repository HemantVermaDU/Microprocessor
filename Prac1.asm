.MODEL SMALL
.386
.DATA
MES1 DB 13,10,'ENTER FIRST NUMBER : $'
MES2 DB 13,10,'ENTER SECOND NUMBER : $'
MES3 DB 13,10,'32 ADDITION IS : $'

.CODE
.STARTUP

MOV AH,09H
MOV DX, OFFSET MES1
INT 21H

MOV CX,8
I1: SHL EBX,4
MOV AH,1
INT 21H
SUB AL,30H
ADD BL,AL
LOOP I1

MOV ESI,EBX
MOV EBX,00000000H

MOV AH,09H
MOV DX, OFFSET MES2
INT 21H

MOV CX,8
I2: SHL EBX,4
MOV AH,1
INT 21H
SUB AL,30H
ADD BL,AL
LOOP I2

MOV AH,09H
MOV DX, OFFSET MES3
INT 21H

MOV EAX,ESI
ADD EAX,EBX
CALL DISPH


.EXIT


DISPH PROC NEAR
PUSH EBX
PUSH ECX
MOV CL,4
MOV CH,8

DISPH1:
ROL EAX,CL
PUSH EAX
AND AL,0FH
ADD AL,30H
CMP AL,'9'
JBE DISPH2
ADD AL,7

DISPH2:
MOV AH,2
MOV DL,AL
INT 21H
POP EAX
DEC CH
JNZ DISPH1
POP ECX
POP EBX
RET

DISPH	ENDP
END
