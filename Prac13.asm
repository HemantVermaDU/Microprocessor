.MODEL SMALL
.DATA
A DW ?
STRING DB 10,13,'ENTER LENGTH OF ARRAY: $'
STRI DB 10,13,'ENTER VALUES: $'
STR1 DB 10,13,'BUBBLE SORTED: $'
ARRAY DB 10 DUP(0)

.CODE
.STARTUP
MOV AH,9
LEA DX,STRING
INT 21H

MOV AH,1
INT 21H
SUB AL,30H
AND AH,00
MOV A,AX
MOV CX,A
 
LEA DX,STRI
INT 21H
MOV BX,OFFSET ARRAY
MOV AH,1

INPUTS:
INT 21H
MOV [BX],AL
INC BX
LOOP INPUTS

MOV CX,A
DEC CX

NEXTSCAN:
MOV BX,CX
MOV SI,0

NEXTCOMP:
MOV AL,ARRAY[SI]
MOV DL,ARRAY[SI+1]
CMP AL,DL
JC NOSWAP

MOV ARRAY[SI],DL
MOV ARRAY[SI+1],AL

NOSWAP:
INC SI
DEC BX
JNZ NEXTCOMP

LOOP NEXTSCAN

MOV AH,9
LEA DX,STR1
INT 21H

MOV CX,A
MOV BX,OFFSET ARRAY

PRINT:
MOV AH,2

MOV DL,[BX]
INT 21H
INC BX
LOOP PRINT

RET
MOV AH,4CH
INT 21H
END